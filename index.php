<?php

// Запуск сессии для работы с пользовательскими данными
session_start();

// Установка часового пояса
date_default_timezone_set('Asia/Yekaterinburg');

// Подключение необходимых файлов-компонентов
require 'dbconnect.php';        // Подключение к базе данных
require 'authentication.php';   // Функции аутентификации пользователя
require 'menu.php';             // Вывод навигационного меню

// Начало основной секции контента с отступом сверху
echo '<main class="container" style="margin-top: 100px">';

// Обработка параметра page из GET-запроса
if (isset($_GET['page'])) {
    switch ($_GET['page']) {
        case 'standings':
            // Проверка авторизации пользователя
            if (isset($_SESSION['login'])) {
                // Загружаем страницу турнирной таблицы
                require 'standings.php';
            } else {
                // Если пользователь не авторизован — выводим сообщение
                $msg = 'Войдите в систему для просмотра и ввода данных';
            }
            break;

        case 'results':
            // Проверка авторизации пользователя
            if (isset($_SESSION['login'])) {
                // Загружаем страницу результатов и форму ввода
                require 'results.php';
                require 'results_form.php';
            } else {
                // Если пользователь не авторизован — выводим сообщение
                $msg = 'Войдите в систему для просмотра и ввода данных';
            }
            break;
    }
}

// Закрытие основной секции контента
echo '</main>';

// Вывод сообщения (если есть)
// Проверяем:
// - есть ли сообщение в сессии
// - или локальная переменная $msg установлена
if ((isset($_SESSION['msg']) && $_SESSION['msg'] != '') || isset($msg)) {
    require 'message.php';  // Подключаем шаблон вывода сообщения
    $_SESSION['msg'] = '';  // Очищаем сообщение в сессии после показа
}

// Подключаем подвал сайта
require 'footer.php';

?>
