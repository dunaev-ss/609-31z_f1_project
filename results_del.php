<?php

// Подключение файла с настройками соединения к базе данных
require 'dbconnect.php';

try {
    // Формируем SQL-запрос на удаление записи из таблицы t_standings
    $sql = 'DELETE FROM t_standings WHERE ID = :id';
    
    // Подготавливаем запрос к выполнению
    $stmt = $conn->prepare($sql);
    
    // Привязываем значение параметра :id из GET-запроса
    $stmt->bindValue(':id', $_POST['id']);
    
    // Выполняем запрос на удаление
    $stmt->execute();
    
    // Устанавливаем сообщение об успешном удалении в сессии
    $_SESSION['msg'] = 'Запись успешно удалена';

} catch (PDOException $error) {
    // Обрабатываем возможные ошибки выполнения запроса
    // Сохраняем сообщение об ошибке в сессии для отображения пользователю
    $_SESSION['msg'] = 'Ошибка удаления записи: ' . $error->getMessage();
}

// Перенаправление пользователя на страницу с результатами
// Используем полный URL для корректной работы редиректа
header('Location: http://localhost/index.php?page=results');

exit();

?>